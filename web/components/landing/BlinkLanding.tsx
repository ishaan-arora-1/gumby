/* ============================================================
   VIDEO WALL
============================================================ */
function VideoWall({
  tiles,
  layout,
}: {
  tiles: Tile[];
  layout: WallLayout;
}) {
  // We use inline `gridTemplateColumns` instead of a Tailwind class because
  // `grid-cols-N` would require N to be statically known at build time and
  // we need to swap it at runtime per breakpoint.
  return (
    <div
      className="absolute z-0 grid gap-3.5"
      style={{
        inset: '-8%',
        transform: 'rotate(-4deg) scale(1.18)',
        transformOrigin: 'center',
        gridTemplateColumns: `repeat(${layout.cols}, minmax(0, 1fr))`,
      }}
      aria-hidden
    >
      {Array.from({ length: layout.cols }).map((_, c) => {
        const dir = c % 2 === 0 ? 'blink-col-up' : 'blink-col-down';
        const slow = c % 3 === 0 ? 'blink-slow' : '';
        // Rotate each column's tile slice by `c` so adjacent same-direction
        // columns (e.g. col 0 and col 2 on mobile) don't show the same
        // videos lined up side-by-side.
        const baseTiles = tiles.slice(c * layout.perCol, c * layout.perCol + layout.perCol);
        const shift = c % baseTiles.length;
        const colTiles = [...baseTiles.slice(shift), ...baseTiles.slice(0, shift)];
        // Negative animation-delay desynchronises the vertical scroll phase
        // so even-direction columns aren't moving in lockstep.
        const phaseDelay = `${-2.3 * c}s`;
        return (
          <div
            key={c}
            className={`flex flex-col gap-3.5 ${dir} ${slow}`}
            style={{ animationDelay: phaseDelay }}
          >
            {[...colTiles, ...colTiles].map((tile, i) => (
              <VTile key={`${c}-${i}`} tile={tile} />
            ))}
          </div>
        );
      })}
    </div>
  );
}

function VTile({ tile }: { tile: Tile }) {
  return (
    <div
      className="relative aspect-[9/16] shrink-0 overflow-hidden rounded-[14px] bg-[#11131a]"
      style={{ boxShadow: '0 18px 50px rgba(0,0,0,0.55)' }}
    >
      {tile.live && (
        <span
          className="blink-live-dot absolute left-[9px] top-[9px] z-[3] h-[7px] w-[7px] rounded-full"
          style={{ background: '#ff2e3f' }}
        />
      )}
      <AutoPlayVideo
        src={tile.src}
        poster={tile.poster}
        className="block h-full w-full object-cover"
      />
    </div>
  );
}

