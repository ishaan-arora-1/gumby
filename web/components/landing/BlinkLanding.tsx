/* Reusable autoplay-when-visible video. Uses IntersectionObserver to
   pause whenever the element is fully off-screen, which is the single
   biggest perf win on this page — without it every <video> on the page
   keeps decoding even when scrolled past, which is what was making the
   site lag. Keep the API tiny so all other video components funnel
   through here. */
function AutoPlayVideo({
  src,
  poster,
  className,
  rootMargin = '200px',
}: {
  src: string;
  poster: string;
  className?: string;
  rootMargin?: string;
}) {
  const ref = useRef<HTMLVideoElement>(null);
  useEffect(() => {
    const v = ref.current;
    if (!v) return;
    // iOS Safari is notoriously strict about autoplay. There is a long-
    // standing React bug where the `muted` JSX prop occasionally doesn't
    // make it onto the DOM element as the `muted` attribute, which causes
    // iOS to refuse autoplay and show a play overlay. Force the property
    // on the element directly to guarantee it's muted before play().
    v.muted = true;
    v.defaultMuted = true;
    v.setAttribute('muted', '');
    v.setAttribute('playsinline', '');
    v.setAttribute('webkit-playsinline', '');
    const tryPlay = () => {
      v.play().catch(() => {});
    };
    tryPlay();
    v.addEventListener('loadedmetadata', tryPlay);
    v.addEventListener('canplay', tryPlay);

    let io: IntersectionObserver | undefined;
    if (typeof IntersectionObserver !== 'undefined') {
      io = new IntersectionObserver(
        ([entry]) => {
          if (entry.isIntersecting) tryPlay();
          else v.pause();
        },
        { rootMargin, threshold: 0 },
      );
      io.observe(v);
    }
    return () => {
      io?.disconnect();
      v.removeEventListener('loadedmetadata', tryPlay);
      v.removeEventListener('canplay', tryPlay);
    };
  }, [rootMargin]);
  return (
    <video
      ref={ref}
      src={src}
      poster={poster}
      autoPlay
      muted
      loop
      playsInline
      preload="auto"
      disablePictureInPicture
      className={className}
    />
  );
}

export function BlinkLanding() {
  const [scrolled, setScrolled] = useState(false);

  useEffect(() => {
    const onScroll = () => setScrolled(window.scrollY > 40);
    window.addEventListener('scroll', onScroll, { passive: true });
    onScroll();
    return () => window.removeEventListener('scroll', onScroll);
  }, []);

  // Preload the unique tile videos + posters as soon as the page mounts
  // so the wall hydrates from cache and plays instantly.
  useEffect(() => {
    const added: HTMLLinkElement[] = [];
    const head = document.head;
    for (const v of TILE_VIDEOS) {
      const lv = document.createElement('link');
      lv.rel = 'preload';
      lv.as = 'video';
      lv.href = v.src;
      lv.type = 'video/mp4';
      head.appendChild(lv);
      added.push(lv);

      const lp = document.createElement('link');
      lp.rel = 'preload';
      lp.as = 'image';
      lp.href = v.poster;
      head.appendChild(lp);
      added.push(lp);
    }
    // Also kick off a hidden fetch on the videos themselves — some
    // browsers ignore <link rel=preload as=video>, but fetch() always
    // primes the HTTP cache for the <video> elements below.
    const ctrl = new AbortController();
    for (const v of TILE_VIDEOS) {
      fetch(v.src, { signal: ctrl.signal, mode: 'no-cors', cache: 'force-cache' }).catch(() => {});
    }
    return () => {
      ctrl.abort();
      for (const el of added) {
        try { head.removeChild(el); } catch {}
      }
    };
  }, []);

  const layout = useWallLayout();

  // Tiles deterministically reuse the 6 real Cloudinary template videos.
  // Every tile sharing the same `src` pulls from the browser cache after
  // the first load, so the wall is effectively instant once the 6 videos
  // are fetched. Tile count varies by breakpoint (see `useWallLayout`).
  const tiles: Tile[] = useMemo(() => {
    const total = layout.cols * layout.perCol;
    return Array.from({ length: total }, (_, i) => {
      const v = TILE_VIDEOS[i % TILE_VIDEOS.length];
      return {
        src: v.src,
        poster: v.poster,
        label: v.label,
        // deterministic "live" dots — every 4th tile, but never the first
        live: i > 0 && i % 4 === 1,
      };
    });
  }, [layout.cols, layout.perCol]);

  return (
    <div className="font-body-blink bg-[#050608] text-white overflow-x-hidden">
      <Nav scrolled={scrolled} />
      <Hero tiles={tiles} layout={layout} />
      <Showcase />
      <FeatureBreakdown />
      <SpeedSection />
      <PricingSection />
      <Faq />
      <FinalCta />
      <Footer />
    </div>
  );
}

